

// if statement
// if statement is used to execute a block of code if a condition is true.
// Syntax:
// if (condition) {
//     // code block to be executed
// }

const x = 6;
switch (x){
    case 1:
        console.log('x is 1');
        break;
    case 2:
        console.log('x is 2');
        break;
    case 3:
        console.log('x is 3');
        break;
    default:
        console.log('x is not 1, 2, or 3');
}