

// if else statement
// if else statement is used to execute a block of code if a condition is true and another block of code if the condition is false.
// Syntax:
// if (condition) {
//     // code block to be executed
// } else {
//     // code block to be executed
// }
// Example:
const x = 5;
if (x === 5) {
    const y = 5;
    console.log(y)
}else {
    console.log('x is not 5')
}